public class TipeDataBoolean {
    public static void main(String[] args) {

        boolean benar = true;
        boolean salah = false;

        if (benar == true) {
            System.out.println("ini benar");
        } else {
            System.out.println("ini salah");
        }

        System.out.println(benar);
        System.out.println(salah);
    }
}
